# ---- make_svp_symmetry_group tests ----


def _make_spin_valley_ops(nb_per_sector: int = 1):
    """Build toy spin/valley operators for nb = 4 * nb_per_sector.

    Returns a dict with keys: identity, s1, s3, v_rotation, v3.
    In this toy model (no orbital structure), v_rotation is just
    Pauli-X in valley space (equivalent to the simple valley flip).
    """
    nb = 4 * nb_per_sector
    # Sector ordering: K↑, K↓, K'↑, K'↓
    # s3: +1 for ↑, -1 for ↓;  v3: +1 for K, -1 for K'
    s3_diag = np.array([+1, -1, +1, -1] * nb_per_sector, dtype=np.float32)
    v3_diag = np.array([+1, +1, -1, -1] * nb_per_sector, dtype=np.float32)

    identity = np.eye(nb, dtype=np.complex64)
    s3 = np.diag(s3_diag.astype(np.complex64))
    v3 = np.diag(v3_diag.astype(np.complex64))

    # s1 flips spin (swaps ↑↔↓ within each valley)
    s1 = np.zeros((nb, nb), dtype=np.complex64)
    for base in range(0, nb, 2 * nb_per_sector):
        for i in range(nb_per_sector):
            s1[base + i, base + nb_per_sector + i] = 1.0
            s1[base + nb_per_sector + i, base + i] = 1.0

    # v_rotation flips valley (swaps K↔K' within each spin).
    # In this toy model there are no orbitals to rotate, so it's
    # just Pauli-X in valley space.
    v_rotation = np.zeros((nb, nb), dtype=np.complex64)
    half = 2 * nb_per_sector
    for i in range(half):
        v_rotation[i, i + half] = 1.0
        v_rotation[i + half, i] = 1.0

    return dict(identity=identity, s1=s1, s3=s3, v_rotation=v_rotation, v3=v3)


def test_svp_group_has_correct_shape():
    ops = _make_spin_valley_ops(nb_per_sector=1)
    same_k, flip_k = make_svp_symmetry_group(
        **{k: jnp.asarray(v) for k, v in ops.items()}
    )
    # 3 same-k elements (even permutations): {I, C1, C2}
    assert same_k.shape == (3, 4, 4)
    # 3 flip-k elements (odd permutations / transpositions): {T_AB, T_AC, T_BC}
    assert flip_k.shape == (3, 4, 4)

    # Each element should be unitary
    for label, arr in [("same_k", same_k), ("flip_k", flip_k)]:
        for i in range(arr.shape[0]):
            g = np.array(arr[i])
            np.testing.assert_allclose(
                g @ g.conj().T, np.eye(4), atol=1e-5,
                err_msg=f"{label}[{i}] not unitary",
            )


def test_svp_group_permutes_three_sectors_fixes_outlier():
    """S₃ averaging should equalize 3 non-outlier diagonal blocks, leave outlier free.

    With a single k-point, k=-k=0, so same-k and flip-k elements are
    equivalent.  This validates the group structure on diagonal matrices.
    """
    ops = _make_spin_valley_ops(nb_per_sector=1)
    same_k, flip_k = make_svp_symmetry_group(
        **{k: jnp.asarray(v) for k, v in ops.items()},
        outlier_sv=(+1, +1),  # K↑ is the outlier
    )
    proj = make_project_fn(
        unitary_group=same_k,
        spatial_group=flip_k,
        spatial_k_convention="flip",
    )

    # Build a diagonal matrix with 4 different sector values
    # Sectors: K↑=0, K↓=1, K'↑=2, K'↓=3
    A = jnp.diag(jnp.array([1.0, 2.0, 3.0, 4.0], dtype=jnp.complex64))
    A = A[None, None, ...]  # (1,1,4,4) — single k-point

    A_proj = proj(A)
    d = jnp.real(jnp.diag(A_proj[0, 0]))

    # K↑ (index 0) should be unchanged: 1.0
    assert float(d[0]) == pytest.approx(1.0, abs=1e-5)
    # K↓, K'↑, K'↓ (indices 1,2,3) should all be averaged: (2+3+4)/3 = 3.0
    assert float(d[1]) == pytest.approx(3.0, abs=1e-5)
    assert float(d[2]) == pytest.approx(3.0, abs=1e-5)
    assert float(d[3]) == pytest.approx(3.0, abs=1e-5)


