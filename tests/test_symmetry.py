def test_combined_unitary_and_time_reversal():
    """Combined group + TR projection should be invariant under both."""
    nk1, nk2, nb = 4, 4, 2
    eye = np.eye(nb, dtype=np.complex64)
    sigma_z = np.diag(np.array([1, -1], dtype=np.complex64))
