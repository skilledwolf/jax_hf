# ---- make_svp_project_fn tests ----


def _make_svp_project_ops(nb_per_sector: int = 1):
    """Build s3, v3 for 4-sector SVP projection tests."""
    nb = 4 * nb_per_sector
    s3 = np.diag(np.tile([+1, +1, -1, -1], nb_per_sector).astype(np.complex64))
    v3 = np.diag(np.tile([+1, -1, +1, -1], nb_per_sector).astype(np.complex64))
    return s3, v3


def test_svp_project_fn_equalises_inactive_blocks():
    """SVP project should equalize 3 inactive blocks and leave outlier free."""
    s3, v3 = _make_svp_project_ops(nb_per_sector=1)
    proj = make_svp_project_fn(
        s3=jnp.asarray(s3),
        v3=jnp.asarray(v3),
        n_orb=1,
        outlier_sv=(+1, +1),
        k_convention="flip",
        k_flip_axes=(0,),
    )

    # Single k-point: kx-flip = identity on 1x1 grid
    A = jnp.diag(jnp.array([1.0, 2.0, 3.0, 4.0], dtype=jnp.complex64))
    A = A[None, None, ...]

    A_proj = proj(A)
    d = jnp.real(jnp.diag(A_proj[0, 0]))

    # Outlier K↑ (index 0) should be unchanged
    assert float(d[0]) == pytest.approx(1.0, abs=1e-5)
    # Inactive blocks (indices 1, 2, 3) should all be averaged: (2+3+4)/3 = 3.0
    assert float(d[1]) == pytest.approx(3.0, abs=1e-5)
    assert float(d[2]) == pytest.approx(3.0, abs=1e-5)
    assert float(d[3]) == pytest.approx(3.0, abs=1e-5)


def test_svp_project_fn_zeros_off_diagonal_blocks():
    """SVP project should zero all inter-flavour coherences."""
    s3, v3 = _make_svp_project_ops(nb_per_sector=1)
    proj = make_svp_project_fn(
        s3=jnp.asarray(s3), v3=jnp.asarray(v3), n_orb=1,
        outlier_sv=(+1, +1), k_convention="flip", k_flip_axes=(0,),
    )

    A = jnp.ones((1, 1, 4, 4), dtype=jnp.complex64)
    A_proj = proj(A)

    # Off-diagonal blocks should be zero
    for i in range(4):
        for j in range(4):
            if i != j:
                assert float(jnp.abs(A_proj[0, 0, i, j])) == pytest.approx(
                    0.0, abs=1e-6
                )


def test_svp_project_fn_does_not_force_k_symmetry_on_outlier():
    """The outlier block should NOT be averaged with its kx-flipped version."""
    s3, v3 = _make_svp_project_ops(nb_per_sector=1)
    proj = make_svp_project_fn(
        s3=jnp.asarray(s3), v3=jnp.asarray(v3), n_orb=1,
        outlier_sv=(+1, +1), k_convention="flip", k_flip_axes=(0,),
    )

    nk1, nk2 = 5, 3
    # Build input where the outlier block varies with kx (NOT kx-symmetric)
    key = jax.random.PRNGKey(42)
    A = jax.random.normal(key, (nk1, nk2, 4, 4)).astype(jnp.complex64)
    # Make Hermitian
    A = 0.5 * (A + jnp.conj(jnp.swapaxes(A, -1, -2)))

    A_proj = proj(A)

    # Outlier block (0, 0) should be EXACTLY the original (not averaged)
    np.testing.assert_allclose(
        np.array(A_proj[..., 0:1, 0:1]),
        np.array(A[..., 0:1, 0:1]),
        atol=1e-7,
    )

    # But the outlier block should NOT have kx-symmetry in general
    outlier_orig = np.array(A[..., 0, 0])  # (nk1, nk2)
    outlier_kx_flip = np.flip(outlier_orig, axis=0)
    assert not np.allclose(outlier_orig, outlier_kx_flip, atol=1e-3)


def test_svp_project_fn_is_idempotent():
    """Applying SVP projection twice should give the same result as once."""
    s3, v3 = _make_svp_project_ops(nb_per_sector=1)
    proj = make_svp_project_fn(
        s3=jnp.asarray(s3), v3=jnp.asarray(v3), n_orb=1,
        outlier_sv=(+1, +1), k_convention="flip", k_flip_axes=(0,),
    )

    nk1, nk2 = 6, 4
    key = jax.random.PRNGKey(99)
    A = jax.random.normal(key, (nk1, nk2, 4, 4)).astype(jnp.complex64)
    A = 0.5 * (A + jnp.conj(jnp.swapaxes(A, -1, -2)))

    once = proj(A)
    twice = proj(once)
    np.testing.assert_allclose(np.array(once), np.array(twice), atol=1e-6)


def test_svp_project_fn_different_outlier():
    """Changing outlier should change which block is left free."""
    s3, v3 = _make_svp_project_ops(nb_per_sector=1)

    # outlier = K'↓ = (-1, -1) = block index 3
    proj = make_svp_project_fn(
        s3=jnp.asarray(s3), v3=jnp.asarray(v3), n_orb=1,
        outlier_sv=(-1, -1), k_convention="flip", k_flip_axes=(0,),
    )

    A = jnp.diag(jnp.array([1.0, 2.0, 3.0, 4.0], dtype=jnp.complex64))
    A = A[None, None, ...]

    A_proj = proj(A)
    d = jnp.real(jnp.diag(A_proj[0, 0]))

    # K'↓ (index 3) should be unchanged: 4.0
    assert float(d[3]) == pytest.approx(4.0, abs=1e-5)
    # K↑, K'↑, K↓ (indices 0, 1, 2) should all be averaged: (1+2+3)/3 = 2.0
    assert float(d[0]) == pytest.approx(2.0, abs=1e-5)
    assert float(d[1]) == pytest.approx(2.0, abs=1e-5)
    assert float(d[2]) == pytest.approx(2.0, abs=1e-5)


